#include <unistd.h>

void	ft_putnbr(int nb)
{
	char temp;

	if (nb < 0)
	{
		write(1, "-", 1);
		nb = (long)nb;
		nb = nb * -1;
	}
	if (nb >= 10)
	{
		ft_putnbr(nb / 10);
		temp = (nb % 10) + '0';
		write(1, &(temp), 1);
	}
	if (nb < 10)
	{
		temp = nb + '0';
		write(1, &(temp), 1);
	}
}
